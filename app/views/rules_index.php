<h1>Ground Rules</h1>

<p class="lead">We are happy to see all sorts of people from all walks of life, participating in this project, that is becoming the sole ruler of our Twitter account for one week. However, there are some ground rules you will have to follow, in order to be nominated as a curator of WeAreUkraine.</p>

<p>Here they are:</p>

<p> 1. Your tweets will have to be exclusively in English. Yes, for some of you this may be a challenge, but otherwise it just cannot work. This is a global project, and as far as international communication is concerned, English is the best means to reach out to and attract as large an audience as possible. Of course, some words in Ukrainian or Russian here and there are absolutely fine.

</p>
<p> 2. You must be an active Twitter user already. This is to make sure you can hit the ground running and concentrate on what you’re doing, rather than trying to figure out what Twitter is all about;)
</p>
<p> 3. You must be ready to tweet at least twice a day, every day throughout a week (starting from Monday till Sunday). Sure enough, the more you respond and engage with your followers, the better, as people will get to know YOU.
</p>
<p> 4. You must be either a current or former UA citizen. However, even if you are neither, but you happen to reside in Ukraine and are willing to abide by the stipulated guidelines, you can also be considered for nomination.
</p>
<p> 5. You must agree to play nice on the account. No fighting, spamming, trolling or being obscene. Generally bear in mind that it is a friendly shared account, and we’re trying to engage people, not alienate them.
</p>
<p>6. Most importantly, enjoy yourself and have fun interacting with the world, while tweeting your week through .
</p>
<p>7. Get involved in this fascinating project.</p>